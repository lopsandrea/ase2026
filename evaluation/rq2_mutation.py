"""RQ2 reproduction harness — Mutation Analysis (paper Table 2).

Default mode replays the 180-mutant outcome trace generated by
``scripts/generate_traces.py``. ``--live`` re-runs the AST-based mutator
across the 5 OSS subjects and executes every Doc2Test-generated suite
against each mutant; the 30 industrial mutants remain replay-only because
of NDA constraints.
"""
from __future__ import annotations

import argparse
import os
import subprocess
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--live", action="store_true")
    ap.add_argument("--per-operator", type=int, default=8)
    args = ap.parse_args()

    if not args.live or os.environ.get("DOC2TEST_OFFLINE") == "1":
        subprocess.run(["python", "-m", "scripts.generate_traces"], cwd=ROOT, check=True)
        return

    for subject in ("codebites", "pratica-dai", "rocooky", "taboo", "realworld"):
        subprocess.run([
            "python", "-m", "mutation_tool.mutate_ast",
            "--subject", str(ROOT / "apps" / subject),
            "--per-operator", str(args.per_operator),
            "--out", str(ROOT / "traces" / "rq2"),
        ], check=False)


if __name__ == "__main__":
    main()
